#include <ctype.h>
#include <math.h>
#include <stdio.h>
#include <string.h>

int convert(string input, int size);

int main(void)
{
    string input = get_string("Enter a positive integer: ");

    for (int i = 0, n = strlen(input); i < n; i++)
    {
        if (!isdigit(input[i]))
        {
            printf("Invalid Input!\n");
            return 1;
        }
    }

    // Convert string to int
    printf("%i\n", convert(input, (int)strlen(input)));
}

int convert(string input, int size)
{
    return (size != 0)? 10*convert(input,size - 1) + (input[size - 1] - '0') : 0;
}
